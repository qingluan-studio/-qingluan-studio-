// ----- 非传统随机引擎 -----
class ChaosRandom {
  private x = 0.1;
  private y = 0.1;
  private z = 0.1;
  private a = 10;
  private b = 28;
  private c = 8/3;

  next(): number {
    const dt = 0.01;
    const dx = this.a * (this.y - this.x) * dt;
    const dy = (this.x * (this.b - this.z) - this.y) * dt;
    const dz = (this.x * this.y - this.c * this.z) * dt;
    this.x += dx;
    this.y += dy;
    this.z += dz;
    return Math.abs(this.x % 1);
  }

  range(min: number, max: number): number {
    return min + this.next() * (max - min);
  }

  pick<T>(arr: T[]): T {
    return arr[Math.floor(this.range(0, arr.length - 0.001))];
  }

  shuffle<T>(arr: T[]): T[] {
    const a = [...arr];
    for (let i = a.length - 1; i > 0; i--) {
      const j = Math.floor(this.range(0, i + 1));
      [a[i], a[j]] = [a[j], a[i]];
    }
    return a;
  }
}

