// ----- 批量生成变体（遗传/进化思想） -----
export function generateLyricVariations(input: LyricInput, count: number = 3): LyricOutput[] {
  const results: LyricOutput[] = [];
  for (let i = 0; i < count; i++) {
    const variant = { ...input, temperature: 0.5 + i * 0.2 };
    results.push(generateLyrics(variant));
  }
  return results;
}

// ----- 格式化输出 -----
export function formatLyrics(output: LyricOutput): string {
  const lines: string[] = [];
  lines.push(`《${output.title}》`);
  lines.push(`情感流: ${output.emotionFlow.map(e => EMOTIONS[e]?.words?.[0] || e).join(' → ')}`);
  lines.push(`韵脚: ${output.rhymeUsed.join(', ')}`);
  lines.push('');

  for (const section of output.sections) {
    lines.push(`[${section.type}]`);
    for (const line of section.lines) {
      lines.push(line);
    }
    lines.push('');
  }

  lines.push(`--- 统计: ${output.stats.totalLines}行 / ${output.stats.totalChars}字 / 平均${output.stats.avgLineLength.toFixed(1)}字/行 ---`);
  return lines.join('\n');
}

